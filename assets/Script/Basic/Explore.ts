const {ccclass, property} = cc._decorator;

@ccclass
export default class Explore extends cc.Component {

   
    start () {
        let _ani = this.getComponent(cc.Animation);
        let self = this;
        _ani.play('bulletExplore')
        
        _ani.on('stop',function(){
            // cc.log(111);
            self.node.destroy();
        })
    }

}
